    public void readTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaFicha.getModel();
        modelo.setNumRows(0);
        FichaDAO fdao = new FichaDAO();
        
        for(Ficha f: fdao.read()){
            
            modelo.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getTitulo(),
                f.getAno(),
                f.getCurso(),
                f.getTipoTrabalho(),
                f.getStatus()
        });
        }
    }
