    public void readTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tableAcervo.getModel();
        modelo.setNumRows(0);
        AcervoDAO adao = new AcervoDAO();
        
        for (Acervo a: adao.read()){
            
            modelo.addRow(new Object[]{
                a.getId(),
                a.getTitulo(),
                a.getEditora(),
                a.getAno(),
                a.getEstante(),
                a.getPrateleira(),
                a.getStatus()
                
            });
        }
    }
   

